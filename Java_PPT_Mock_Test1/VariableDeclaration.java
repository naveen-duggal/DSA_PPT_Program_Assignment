public class VariableDeclaration {
    public static void main(String[] args) {
        int age = 22;
        double height = 5.7;
        String name = "Naveen Duggal";

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height + " meters");
    }
}